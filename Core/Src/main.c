uint32_t CalcFTW(uint32_t freq)
{
	uint32_t FTW;
	float refFreq = 16000000;
	float FTWResolution = 0x10000000;

	FTW = (FTWResolution / refFreq) * freq;
	return FTW;
}

void writeRegister(uint16_t data)
{
    HAL_GPIO_WritePin(FG_FSYNC_GPIO_Port, FG_FSYNC_Pin, 0);
    HAL_SPI_Transmit(&hspi2, (uint8_t *)&data, 1, HAL_MAX_DELAY);
    HAL_GPIO_WritePin(FG_FSYNC_GPIO_Port, FG_FSYNC_Pin, 1);
}

void sineWave(uint32_t FTW)
{
	int MSB;
	int LSB;
	int phase = 0;

	// Prepare Bytes for wave generation
	MSB = (uint32_t)((FTW & 0xFFFC000)>>14); //14 bits
	LSB = (uint32_t)(FTW & 0x3FFF);

	// DB15, DB14 Control Bits 0, 1 -> FREQ0 register
	LSB |= 0x4000;
	MSB |= 0x4000;
	phase &= 0xC000;

	writeRegister(0x2100);
	writeRegister(LSB);
	writeRegister(MSB);
	writeRegister(phase);

	writeRegister(0x2000);
}

void delayUs (uint16_t us) {
	__HAL_TIM_SET_COUNTER (&htim1, 0);
	while (__HAL_TIM_GET_COUNTER(&htim1) < us);
}

